let _currentLang = "en";

export function getLang() {
  return _currentLang;
}

export function setLang(lang) {
  _currentLang = lang === "he" ? "he" : "en";
}

export function t(key) {
  const tr = TRANSLATIONS[_currentLang];
  return (tr && tr[key]) || TRANSLATIONS.en[key] || key;
}

export function applyLanguage(lang) {
  setLang(lang);
  const L = lang === "he" ? "he" : "en";
  const root = document.documentElement;
  root.setAttribute("lang", L === "he" ? "he" : "en");
  root.setAttribute("dir", L === "he" ? "rtl" : "ltr");
  document.body.classList.toggle("lang-he", L === "he");
  document.body.classList.toggle("lang-en", L === "en");

  const tr = TRANSLATIONS[L] || TRANSLATIONS.en;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (key && tr[key]) el.textContent = tr[key];
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (key && tr[key]) el.placeholder = tr[key];
  });
  document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
    const key = el.getAttribute("data-i18n-aria");
    if (key && tr[key]) el.setAttribute("aria-label", tr[key]);
  });

  const titleEl = document.querySelector("title");
  if (titleEl && tr.docTitle) titleEl.textContent = tr.docTitle;
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc && tr.docDescription) metaDesc.setAttribute("content", tr.docDescription);
  document.dispatchEvent(new CustomEvent("i18n:applied", { detail: { lang: L } }));
}
